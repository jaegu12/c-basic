#include <stdio.h>

/**
 *  숫자: 5자리
 *  예: 57256
 * 
*/
int main(void) {
    int num = 46324;
    int total = 0;
    
    
    
    // 46324 → 10 몫: 46324 나머지: 4
    // 4632 → 10 몫: 463 나머지: 2
    // 463 → 10 몫: 46 나머지: 3
    // 46 → 10 몫: 4 나머지: 6 
    // 4 → 10 몫: 0 나머지: 4

    // 5번 반복
    for(int i=1; i<=5; i++) {
        num = num / 10;
        total += (num % 10); //총합 
    }
    printf("총합 %d\n", total);

}